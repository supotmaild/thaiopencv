def pixel(x,y):
	global cx,button,pix,editArea
	editArea.delete('1.0', 'end-1c')
	if pix[cx][x][y]==0:
		pix[cx][x][y]=1
		button[x][y].configure(bg='#0000ff')
	else:
		pix[cx][x][y]=0
		button[x][y].configure(bg='#e5e4e2')

	# in reallife LCD use vertical line byte
	byte_text='def font():\n'+chr(9)+'pixel=[['
	for c in range(256):
		byte=[]
		if c!=0:
			byte_text=byte_text+chr(9)+'['
		# Horizontal
		for i in range(8):
			byte.append(0)
			# Vetical
			for j in range(8):
				if pix[c][j][i]==1:
					byte[i] = (2**(7-j))+byte[i]
			byte_text=byte_text+hex(byte[i])+', '
		if len(byte_text)>2:
			byte_text=byte_text[:-2]
			byte_text=byte_text+'],\n'
	byte_text=byte_text[:-2]+']\n'+chr(9)+'return pixel'
	editArea.insert(tk.END,byte_text)
	f = open('lcd_font.py', 'w+')
	f.write(byte_text)
	f.close()

